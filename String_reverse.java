package Java_Practice;

import java.util.Scanner;

public class String_reverse {

	public static void main(String[] args) {
	     String str="Hello";
	      
	     char[] Chararray=str.toCharArray();
	     
	     for (int i =Chararray.length-1;i>=0;i--) {
	    	System.out.print(Chararray[i] + " ");
			
		}
	     }
	       

	}


